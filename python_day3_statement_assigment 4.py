# 4. Find the Simple Interest on Rs. 200 for 5 years at 5% per year.


# Given data for Simple Interest calculation
principal = 200  # Principal amount in Rs.
rate_of_interest = 5  # Rate of interest per year in percentage
time_period = 5  # Time period in years

# Simple Interest formula: SI = (Principal * Rate * Time) / 100
simple_interest = (principal * rate_of_interest * time_period) / 100

# printing the result
print(f"The Simple Interest is: Rs. {simple_interest}")

#Output
"""
The Simple Interest is: Rs. 50.0

"""
